package br.unicamp.ic.mc437.model;

public class Mutant {
    private Integer id;
    private Boolean success;

    public Mutant() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Boolean getSuccess() {
        return success;
    }

    public void setSuccess(Boolean success) {
        this.success = success;
    }
}
